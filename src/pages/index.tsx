const apiCall = async (
  data: string,
  setLoading: (boolean: boolean) => void
) => {
  setLoading(true);
  const res = await fetch("/api/api", {
    method: "POST",
    headers: {
      Accept: "application/json, text/plain, */*",
      "Content-Type": "application/json",
    },
    body: JSON.stringify(data),
  });

  setLoading(false);
  if (res.status === 200) {
    console.log("Response succeeded!");
    return res.json();
  } else {
    return;
  }
};

